function buildPlaceholderPrompt(p: Placeholder, now: number): Prompt {
  return {
    id: p.id,
    title: p.title,
    body: p.body,
    summary: p.summary,
    primaryScenario: [...p.path],
    secondaryScenarios: [],
    taskType: p.taskType,
    inputRequirements: p.inputRequirements,
    outputFormat: p.outputFormat,
    upstreamPrompts: [],
    downstreamPrompts: [],
    recommendedCombinations: [],
    boundaries: "",
    tags: [p.path[0].replace(/场景$/, ""), p.path[1]],
    difficulty: "初级",
    valueLevel: "高频",
    shareable: false,
    isFavorited: false,
    useCount: 0,
    lastUsedAt: null,
    createdAt: now,
    updatedAt: now,
    pendingReview: false,
  };
}

/** 启动时调用：按 id 检查存在性，缺什么补什么；存在但内容陈旧的自动升级。 */
export async function seedPlaceholderPrompts() {
  const { db } = await import("../index");
  const now = Date.now();
  const existingPrompts = await db.prompts.where("id").anyOf(PLACEHOLDERS.map((p) => p.id)).toArray();
  const existingMap = new Map(existingPrompts.map((p) => [p.id, p]));
  const toInsert = PLACEHOLDERS
    .filter((p) => !existingMap.has(p.id))
    .map((p) => buildPlaceholderPrompt(p, now));
  if (toInsert.length) {
    await db.prompts.bulkAdd(toInsert);
    if (import.meta.env.DEV) console.log(`[Prompt OS] 已补齐 ${toInsert.length} 条占位 prompt`);
  }

  // 把旧版骨架（body/summary 为模板字符串）升级为新内容
  const stale = existingPrompts.filter((p) => {
    const summary = p.summary ?? "";
    const body = p.body ?? "";
    return (
      summary.startsWith("占位骨架") ||
      summary.startsWith("骨架模板") ||
      body.startsWith("请帮我") && body.includes("[在这里描述输入信息]")
    );
  });
  if (stale.length) {
    await Promise.all(
      stale.map((p) => {
        const fresh = PLACEHOLDERS.find((ph) => ph.id === p.id);
        if (!fresh) return Promise.resolve();
        return db.prompts.update(p.id, {
          body: fresh.body,
          summary: fresh.summary,
          inputRequirements: fresh.inputRequirements,
          outputFormat: fresh.outputFormat,
          updatedAt: now,
        });
      })
    );
    if (import.meta.env.DEV) console.log(`[Prompt OS] 已升级 ${stale.length} 条占位 prompt 为正式内容`);
  }
}
